/* Console Section */

//Clearing the console
console.clear();

//Starting the timer for this section of code
console.time('console section time');

//Logging a string to the console
console.log("Hello World!");

//Logging a number to the console
console.log(9);

//Logging a boolean to the console
console.log(true);

//Logging a array to the console
console.log([0, 1, 2, 3, 4, 5]);

//Logging a object to the console
console.log({name: "Cam", age: 27, hair: "red"});

//Logging data in table format
console.table({a: 1, b:2, c:3});

//Logging a warning message
console.warn("This is a warning message");

//Logging an error message
console.error("This is an error message");

//Ending the timer for this section of code
console.timeEnd('console section time');
